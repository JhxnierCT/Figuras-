package ec.edu.espoch.figuras.Cuadrado;

/**
 *
 * @author COTAYA
 */
public class Cuadrados {

    public double lado;

    public Cuadrados(double lado) {
        this.lado = lado;
    }

    public double calcularArea() {
        return lado * lado;
    }

    public double calcularPerimetro() {
        return 4 * lado;

    }
}
